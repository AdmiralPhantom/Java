public class Loops {
    public static void main(String[] args){
        //for loop
        for(int i=10; i>1; i--){
        System.out.println("The value of i is:"+i);
    }
    //while loops
    int j=0;
    while(j>11){
        System.out.println("The value of J is:"+j);
        j++;
    }
    }
}
